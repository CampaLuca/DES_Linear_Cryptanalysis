int table[64][16];

uint8_t bitmask(uint8_t alpha, uint8_t x, int bit_length) {
    uint8_t s = 0;
    for (int i = 0; i < bit_length; i++) {
        s ^= ((x >> i) & 0x1) & ((alpha >> i) & 0x1);

    }
    //rintf("%d",s);
    return s;
}

void mask_table(uint8_t sbox_index) {
    
        for (uint8_t alpha = 1; alpha < 64; alpha++) {

            for (uint8_t beta = 1; beta < 16; beta++) {

                for (uint8_t x = 0; x < 64; x++) {

                    uint8_t x_cord = 0;
                    uint8_t y_cord = 0;

                    x_cord = x_cord + (x & 0x1) + (((x >> 5) & 0x1) << 1);
                    y_cord = y_cord + ((x & 0b11110) >> 1);


                    uint8_t output = DesSbox[sbox_index][x_cord][y_cord];
                    if (bitmask(alpha, x, 6) == bitmask(beta, output, 4)) {
                        table[alpha][beta] += 1;
                    }
                }
            }
        }
    

}



